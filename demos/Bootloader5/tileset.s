#include <avr/io.h>



.global Tileset_Load



;
; Loads the tileset into the tileset area. It must be zero before loading.
;
; Clobbers:
; r0, r22, r23, r24, r25, XL, XH, ZL, ZH
;
.section .text.Tileset_Load
Tileset_Load:

	ldi   ZL,      lo8(tileset_data)
	ldi   ZH,      hi8(tileset_data)
	ldi   XL,      lo8(tileset)
	ldi   XH,      hi8(tileset)
	ldi   r24,     TILESET_SIZE
romcv_till:
	ldi   r23,     5
romcv_coll:
	ldi   r22,     8
	lpm   r25,     Z+
romcv_rowl:
	ld    r0,      X
	lsr   r25
	ror   r0
	st    X+,      r0
	dec   r22
	brne  romcv_rowl
	sbiw  XL,      8
	dec   r23
	brne  romcv_coll
	adiw  XL,      8
	dec   r24
	brne  romcv_till
	ret



